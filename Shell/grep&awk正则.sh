#!/bin/bash

awk '/^([0-9]{3}-|\([0-9]{3}\) )[0-9]{3}-[0-9]{4}$/' file.txt

#-P  使用正则
grep -P '^([0-9]{3}-|\([0-9]{3}\) )[0-9]{3}-[0-9]{4}$' file.txt

# 以匹配电话为例
# (xxx) xxx-xxxx 或 xxx-xxx-xxxx。（x 表示一个数字）

# 正则表达式的重点有三：特殊字符、限定字符、定位符
# 特殊字符 + \
# 限定字符 {num} 次数
# 定位符 规定位置

# 分析 (xxx) xxx-xxxx
# 固定存在的"()"   " "    "-"
# xxx-xxx-xxxx
# 固定存在的"-"
# ^\([0-9][0-9][0-9]\) ...-....$
# ^[0-9][0-9][0-9]-...-....$
# 加限定符 ^(\([0-9]{3}\) |[0-9]{3}-)[0-9]{3}-[0-9]{4}$
